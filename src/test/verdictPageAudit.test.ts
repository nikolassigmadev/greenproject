// ── Findings collector ───────────────────────────────────────────────────────

interface Finding {
  level: "ERROR" | "WARN" | "INFO";
  area: string;
  message: string;
}
const findings: Finding[] = [];
const report = (level: Finding["level"], area: string, message: string) => {
  findings.push({ level, area, message });
};

afterAll(() => {
  const lines: string[] = [
    "# Verdict-page audit report",
    "",
    `Generated by \`src/test/verdictPageAudit.test.ts\` on ${new Date().toISOString().slice(0, 10)}.`,
    "",
    `Totals: ${findings.filter(f => f.level === "ERROR").length} errors, ` +
      `${findings.filter(f => f.level === "WARN").length} warnings, ` +
      `${findings.filter(f => f.level === "INFO").length} notes.`,
    "",
  ];
  for (const level of ["ERROR", "WARN", "INFO"] as const) {
    const group = findings.filter(f => f.level === level);
    if (group.length === 0) continue;
    lines.push(`## ${level}`, "");
    for (const f of group) lines.push(`- **[${f.area}]** ${f.message}`);
    lines.push("");
  }
  const out = path.resolve(__dirname, "../../docs/verdict-audit-report.md");
  fs.writeFileSync(out, lines.join("\n"));
  // eslint-disable-next-line no-console
  console.log(`\n[verdict-audit] ${findings.length} findings → docs/verdict-audit-report.md`);
});

// ── Product + priority fixtures ──────────────────────────────────────────────

function makeProduct(over: Partial<OpenFoodFactsResult>): OpenFoodFactsResult {
  return {
    found: true,
    barcode: "0000000000000",
    productName: null,
    brand: null,
    ecoscoreGrade: null,
    ecoscoreScore: null,
    nutriscoreGrade: null,
    nutriscoreScore: null,
    novaGroup: null,
    carbonFootprint100g: null,
    carbonFootprintProduct: null,
    carbonFootprintServing: null,
    labels: [],
    categories: [],
    origins: null,
    ingredientsText: null,
    imageUrl: null,
    ecoscoreData: null,
    rawProduct: null,
    ...over,
  };
}

const P_DEFAULT: UserPriorities = { ...DEFAULT_PRIORITIES };
const P_ETHICS_CRITICAL: UserPriorities = { ...DEFAULT_PRIORITIES, laborRights: 80, animalWelfare: 80, environment: 50 };
const P_ECO_FOCUSED: UserPriorities = { ...DEFAULT_PRIORITIES, laborRights: 20, animalWelfare: 20, environment: 80 };
const PRIORITY_SETS: [string, UserPriorities][] = [
  ["default", P_DEFAULT],
  ["ethics-critical", P_ETHICS_CRITICAL],
  ["eco-focused", P_ECO_FOCUSED],
];

const VALID_VERDICTS = new Set(["BUY", "CONSIDER", "CAUTION", "AVOID", "UNKNOWN"]);
const BAND_RANK: Record<string, number> = { AVOID: 0, CAUTION: 1, CONSIDER: 2, BUY: 3 };

// Mirrors LABOUR_FLAG_CATEGORIES in src/services/swaps/index.ts.
const LABOUR_FLAG_CATEGORIES = new Set([
  "forced_labour", "child_labour", "wage_theft", "unsafe_conditions", "union_busting",
]);

// ── Brand universe ───────────────────────────────────────────────────────────

interface SourcedCandidate {
  candidate: AltCandidate;
  source: string;
  category: SwapCategoryKey;
}

function allSwapCandidates(): SourcedCandidate[] {
  const out: SourcedCandidate[] = [];
  for (const cat of SWAP_CATEGORY_KEYS) {
    for (const c of ETHICAL_ALTERNATIVES[cat]) out.push({ candidate: c, source: "catalog", category: cat });
    for (const c of getCustomCandidates(cat)) out.push({ candidate: c, source: "customSwaps", category: cat });
    for (const c of getVerifiedEthicsCandidates(cat)) out.push({ candidate: c, source: "verifiedEthics", category: cat });
    for (const c of getChocolateDirectoryCandidates(cat)) out.push({ candidate: c, source: "chocolateDirectory", category: cat });
  }
  return out;
}

