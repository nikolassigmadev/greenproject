// @vitest-environment jsdom
//
// Verdict-page audit harness.
//
// Runs the REAL logic behind the product-detail (verdict) page across hundreds
// of synthetic products built from every brand the app knows about, and
// cross-checks every signal the page renders:
//
//   - top-line verdict            getVerdict() (OpenFoodFactsDetail)
//   - personalized numeric score  personalizedScore()
//   - labour allegations banner   findLaborAllegations()
//   - verified brand-flag banner  getVerifiedFlagsForBrand()
//   - boycott banner              checkBoycott()
//   - animal-welfare flag         checkAnimalWelfareFlag()
//   - verified-ethics green card  findVerifiedEthics()
//   - chocolate directory verdict findChocolateEntry()
//   - ingredient concerns         findIngredientFlagsInText()
//   - swap suggestions            the four candidate sources
//
// It fails when the app contradicts itself — e.g. a brand the app itself
// boycott-flags being recommended as an "ethical swap", or a matcher
// false-positive that would put a red banner on an innocent brand.
// A full findings report is written to docs/verdict-audit-report.md.

